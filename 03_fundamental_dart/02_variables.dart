// variable => adalah wadah atau tempat untuk menyimpan nilai.

void main() {
  var greetings = "Hello Dart";
  print(greetings);
  // tanda '=' ini disebut assignment operator (memasukkan nilai ke variabel)
  // proses ini disebut inisialisasi

  // var bisa untuk string atau juga untuk num
  var myAge = 20;
  print(myAge);

  // kalau tidak ingin langsung menginisialisasi kita bisa mendeklarasikan dulu
  // baru menginisalisasinya.
  var age; // => deklarasi 
  age = 20; // => inisialisasi
  print(age);
}
